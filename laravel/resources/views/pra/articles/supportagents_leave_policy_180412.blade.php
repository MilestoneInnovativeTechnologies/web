<h2 style="text-transform: uppercase"><u>Milestone Innovative Technologies - Leave Policy</u></h2>
<h3>TYPE OF LEAVE</h3>
<ul>
	<li>Casual Leave of 12 days for special or unforeseen circumstances.</li>
	<li>Earned leave of 12 days for annual relaxation, recouping and to fulfil personal, social or religious obligation.</li>
</ul>
<h3>CASUAL LEAVE AND SICK LEAVE (CL)</h3>
<ul>
	<li>Leave taken to a maximum of 2 consecutive days shall be considered as Casual Leave.</li>
	<li>Casual Leave and Earned Leave cannot be clubbed together.</li>
	<li>Employees availing CL for more than 2 days for medical reasons should produce Medical Certificate.</li>
</ul>
<h3>EARNED LEAVE (EL)</h3>
<ul>
	<li>Leave taken for more than 2 consecutive days will be considered as Earned Leave.</li>
	<li>Every employee shall be eligible for one day of earned leave for every thirty days worked.</li>
	<li>EL may be granted to the employees on probation on the following circumstances
		<ul>
			<li>Marriage of employee / employee’s sibling.</li>
			<li>Sickness leading to the hospitalization of the employee / employee’s dependent.</li>
			<li>Death in the immediate family.</li>
			<li>Examination.</li>
			<li>In case of becoming a proud father.</li>
		</ul>
	</li>
	<li>Such leave shall be granted to the extent of pro-rata leave accrued by the employee and excess leave taken will be accounted as LOP.</li>
	<li>Earned Leave can be accumulated to a maximum of 60 days. </li>
</ul>
<h3>PARTIAL ATTENDANCE</h3>
<ul>
	<li>Employees arriving ten minutes late than the starting time will be given a black mark in their register and three such marks in a month shall be considered as a half day leave</li>
	<li>Employees leaving up to 30 minutes earlier than the usual leaving time shall be given a black mark and those leaving before that will be considered a half day leave</li>
	<li>Employees taking a break for half an hour shall be given a black mark and a break for 3 hours shall be considered as a half day leave</li>
</ul>
<h3>PROCEDURE TO AVAIL LEAVE</h3>
<ul>
	<li>Employees shall avail leave only on prior approval of the Reporting Manger.</li>
	<li>The approval mail must be forwarded to hr@milestoneit.net </li>
	<li>Approval for CL and EL should be taken at the least of one day and three days in advance respectively. </li>
</ul>
<h3>LOSS OF PAY</h3>
<ul>
	<li>Employees availing leave more than the number of days to their credit will have a loss of pay to the extent of excess leave taken.</li>
	<li>Leave taken without the written approval of the Reporting Manager (unless it is an emergency) will be considered as absent and is subject to loss of pay.</li>
	<li>Leave not being notified to HR department through hr@milestoneit.net with approval of Reporting Manager will amount to loss of pay in the subsequent month/pay cycle.</li>
	<li>If LOP is more than 30 days that will impact incentive/Managers commission.(Applicable only for managers)</li>
</ul>
<h3>LEAVE ENCASHMENT</h3>
<ul>
	<li>Un-availed CL of employees with less than one year tenure with the organization shall be encashed at the end of every financial year.</li>
	<li>Employees who have completed one year shall have an option to convert un-availed CL to EL or encash at the end of financial year.</li>
	<li>A maximum of 6 CL can be encashed or converted to EL for a year.</li>
	<li>Accumulated EL shall be encashed at the time of separation.</li>
	<li>Encashment of leave is calculated on the salary of the employee. The salary per day will be arrived at by dividing the salary by 30.</li>
	<li>In case of separation, un-availed CL for the financial year will lapse.</li>
</ul>

